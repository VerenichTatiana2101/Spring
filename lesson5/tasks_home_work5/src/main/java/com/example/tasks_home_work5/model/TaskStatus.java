package com.example.tasks_home_work5.model;

/**
 * Перечисление TaskStatus определяет возможные статусы задачи.
 * Задача может быть в статусе:
 * NOT_STARTED - "не начата",
 * IN_PROGRESS - "в процессе"
 * COMPLETED - "завершена".
 */
public enum TaskStatus {
    NOT_STARTED,
    IN_PROGRESS,
    COMPLETED;
}